package exemplo1;

public class Exemplo1 {
	public static void main(String[] args) {
		float x = Integer.MAX_VALUE;
		double y = Integer.MAX_VALUE;
		
		System.out.println("Float = "+x);
		System.out.println("Double = "+y);
		
		
		x = Float.MAX_VALUE;
		y = Double.MAX_VALUE;
		
		System.out.println("Float = "+x);
		System.out.println("Double = "+y);
	}
}
