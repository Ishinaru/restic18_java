package poligonos3D_ruim;

class Ponto {
    private float x, y, z;
    Ponto(float _x, float _y, float _z) {
        x = _x;
        y = _y;
        z = _z;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }
}
